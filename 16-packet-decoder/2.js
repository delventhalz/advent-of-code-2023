// Now that you have the structure of your transmission decoded, you can
// calculate the value of the expression it represents.

// Literal values (type ID 4) represent a single number as described above. The
// remaining type IDs are more interesting:

// - Packets with type ID 0 are sum packets - their value is the sum of the
//   values of their sub-packets. If they only have a single sub-packet, their
//   value is the value of the sub-packet.
// - Packets with type ID 1 are product packets - their value is the result of
//   multiplying together the values of their sub-packets. If they only have a
//   single sub-packet, their value is the value of the sub-packet.
// - Packets with type ID 2 are minimum packets - their value is the minimum of
//   the values of their sub-packets.
// - Packets with type ID 3 are maximum packets - their value is the maximum of
//   the values of their sub-packets.
// - Packets with type ID 5 are greater than packets - their value is 1 if the
//   value of the first sub-packet is greater than the value of the second
//   sub-packet; otherwise, their value is 0. These packets always have exactly
//   two sub-packets.
// - Packets with type ID 6 are less than packets - their value is 1 if the
//   value of the first sub-packet is less than the value of the second
//   sub-packet; otherwise, their value is 0. These packets always have exactly
//   two sub-packets.
// - Packets with type ID 7 are equal to packets - their value is 1 if the
//   value of the first sub-packet is equal to the value of the second
//   sub-packet; otherwise, their value is 0. These packets always have exactly
//   two sub-packets.

// Using these rules, you can now work out the value of the outermost packet in
// your BITS transmission.

// For example:

// - C200B40A82 finds the sum of 1 and 2, resulting in the value 3.
// - 04005AC33890 finds the product of 6 and 9, resulting in the value 54.
// - 880086C3E88112 finds the minimum of 7, 8, and 9, resulting in the value 7.
// - CE00C43D881120 finds the maximum of 7, 8, and 9, resulting in the value 9.
// - D8005AC2A8F0 produces 1, because 5 is less than 15.
// - F600BC2D8F produces 0, because 5 is not greater than 15.
// - 9C005AC2F8F0 produces 0, because 5 is not equal to 15.
// - 9C0141080250320F1802104A08 produces 1, because 1 + 3 = 2 * 2.

// What do you get if you evaluate the expression represented by your
// hexadecimal-encoded BITS transmission?

const { sum } = require('../lib');


const TYPES = {
  0: 'SUM',
  1: 'PRODUCT',
  2: 'MIN',
  3: 'MAX',
  4: 'LITERAL',
  5: 'GREATER_THAN',
  6: 'LESS_THAN',
  7: 'EQUAL_TO'
};

const OPERATIONS = {
  SUM: sum,
  PRODUCT: nums => nums.reduce((x, y) => x * y),
  MIN: nums => Math.min(...nums),
  MAX: nums => Math.max(...nums),
  GREATER_THAN: ([x, y]) => x > y ? 1 : 0,
  LESS_THAN: ([x, y]) => x < y ? 1 : 0,
  EQUAL_TO: ([x, y]) => x === y ? 1 : 0
};


const hexToBinary = (hex) => {
  let binary = '';
  for (const char of hex) {
    binary += parseInt(char, 16).toString(2).padStart(4, '0');
  }
  return binary;
};

const toDecimal = binary => parseInt(binary, 2);

const getParser = (parseFn) => (binary) => {
  const version = toDecimal(binary.slice(0, 3));
  const type = TYPES[toDecimal(binary.slice(3, 6))];
  const { length, ...packet } = parseFn(binary.slice(6));

  return {
    version,
    type,
    ...packet,
    length: length + 6
  };
};

const parseLiteral = getParser((binary) => {
  let i = 0;
  let bits = '';

  while(true) {
    bits += binary.slice(i + 1, i + 5);

    if (binary[i] === '0') {
      return {
        value: toDecimal(bits),
        length: i + 5
      };
    }

    i += 5;
  }
});

const getOperands = (binary, hasMore) => {
  let operands = [];
  let length = 0;

  while (hasMore({ length, operands })) {
    const packet = parsePacket(binary.slice(length));
    length += packet.length;
    operands.push(packet);
  }

  return { length , operands };
};

const parseOperator = getParser((binary) => {
  const headerLength = binary[0] === '0' ? 15 + 1 : 11 + 1;

  const size = toDecimal(binary.slice(1, headerLength));
  const hasMore = binary[0] === '0'
    ? ({ length }) => length < size
    : ({ operands }) => operands.length < size;

  const { length, operands } = getOperands(binary.slice(headerLength), hasMore);
  return {
    operands,
    length: length + headerLength
  };
});

const parsePacket = (binary) => {
  const typeNumber = toDecimal(binary.slice(3, 6));

  if (TYPES[typeNumber] === 'LITERAL') {
    return parseLiteral(binary);
  }

  return parseOperator(binary);
};

const runPacket = ({ type, operands, value }) => {
  if (type === 'LITERAL') {
    return value;
  }

  return OPERATIONS[type](operands.map(runPacket));
};


module.exports = (_, rawInput) => {
  const binary = hexToBinary(rawInput);
  const packet = parsePacket(binary);

  return runPacket(packet);
};

